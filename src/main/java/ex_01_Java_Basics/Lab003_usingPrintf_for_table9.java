package ex_01_Java_Basics;

public class Lab003_usingPrintf_for_table9 {
    public static void main(String[] args) {
        int n=9,n2,n3;

        System.out.printf("%d*1=%d\n",n,n);
        System.out.printf("%d*2=%d\n",n,n2=n*2);
        System.out.printf("%d*3=%d\n",n,n3=n*3);
        System.out.printf("%d*%d=%d\n",n,4,n*4);
        System.out.printf("%d*%d=%d\n",n,5,n*5);
        System.out.printf("%d*6=%d\n",n,n*6);
        System.out.printf("%d*7=%d\n",n,n*7);
        System.out.printf("%d*8=%d\n",n,n*8);
        System.out.printf("%d*9=%d\n",n,n*9);
        System.out.printf("%d*10=%d\n",n,n*10);
    }
}
